<?php

    //------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
    function _money($value)
    {
        $temp = "$" . number_format($value,"2",",",".");
        return($temp);
    }
    //------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
    function _number($value)
    {
        $temp = number_format($value,"2",",",".");
        return($temp);
    }
    //------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
    function _date($date)
    {
        $temp   = explode("-",$date);
        $temp   = $temp[2] . "/" . $temp[1] . "/" . $temp[0];
        return($temp);
    }
    //------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
    function _datetime($dateTime)
    {
        $date   = explode(" ",$dateTime)[0];
        $time   = explode(" ",$dateTime)[1];

        $temp   = explode("-",$date);
        $date   = $temp[2] . "/" . $temp[1] . "/" . $temp[0];

        return($date . " " . $time);
    }
    //------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
    function timestamp()
    {
        return(date("Y-m-d H:i:s"));
    }
    //------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
?>